use x11::keysym::*;

macro_rules! handler_fns {
    ($($id:ident$(,)*)*) => {
        [
            $((stringify!($id), stringify!($id)),)*
        ]
    }
}

macro_rules! keys {
    ($($id:ident$(,)*)*) => {
        [
            $((stringify!($id), $id),)*
        ]
    }
}

/// Turn [Clk] variants like `Clk::TagBar` into string (`ClkTagBar`), `Int`
/// pairs
macro_rules! clicks {
    ($($id:ident$(,)*)*) => {
        [
            $((concat!("Clk", stringify!($id)), Clk::$id as u32),)*
        ]
    }
}

